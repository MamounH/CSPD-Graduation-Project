package com.example.admincivil.Employee.PassportServices.Renewal;

public class RenewalClass {
    public String imagePassport ;
    public String passportNumber;
    public String datePassport ;
    public String country ;
    public String address ;
    public  String status ;
    public String pushKey ;
    public String nationalId;
    public RenewalClass() {
    }

    public String getImagePassport() {
        return imagePassport;
    }

    public String getPassportNumber() {
        return passportNumber;
    }

    public String getDatePassport() {
        return datePassport;
    }

    public String getCountry() {
        return country;
    }

    public String getAddress() {
        return address;
    }

    public String getStatus() {
        return status;
    }

    public String getPushKey() {
        return pushKey;
    }

    public String getNationalId() {
        return nationalId;
    }
}
