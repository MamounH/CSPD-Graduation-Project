package com.example.civilaffairs.ui.DepartmentServices.FamilyServices;

public class FamilyClass {

    public String pushKey ;
    public String urlImage;
    public String paterfamilias;
    public String numberPaterfamilias;
    public String bookNumber;
    public String placeIssue;
    public String releaseDate;
    public String address;
    public String status;

    public FamilyClass() {
    }

    public void setPushKey(String pushKey) {
        this.pushKey = pushKey;
    }

    public void setUrlImage(String urlImage) {
        this.urlImage = urlImage;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public void setPaterfamilias(String paterfamilias) {
        this.paterfamilias = paterfamilias;
    }

    public void setNumberPaterfamilias(String numberPaterfamilias) {
        this.numberPaterfamilias = numberPaterfamilias;
    }

    public void setBookNumber(String bookNumber) {
        this.bookNumber = bookNumber;
    }

    public void setPlaceIssue(String placeIssue) {
        this.placeIssue = placeIssue;
    }

    public void setReleaseDate(String releaseDate) {
        this.releaseDate = releaseDate;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
