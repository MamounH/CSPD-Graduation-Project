package com.example.civilaffairs.ui.DepartmentServices.PassportServices.AddEnum;

class AddEnumClass {

    public String imagePassportUrl;
    public String passportNumber;
    public String imageBirthCertificateUrl;
    public String childName;
    public String pushKey;
    public String status;
    public String nationalId;

    AddEnumClass() {
    }

    void setImagePassportUrl(String imagePassportUrl) {
        this.imagePassportUrl = imagePassportUrl;
    }

    void setPassportNumber(String passportNumber) {
        this.passportNumber = passportNumber;
    }

    void setImageBirthCertificateUrl(String imageBirthCertificateUrl) {
        this.imageBirthCertificateUrl = imageBirthCertificateUrl;
    }

    void setChildName(String childName) {
        this.childName = childName;
    }

    void setPushKey(String pushKey) {
        this.pushKey = pushKey;
    }

    void setStatus(String status) {
        this.status = status;
    }

    public void setNationalId(String nationalId) {
        this.nationalId = nationalId;
    }
}
