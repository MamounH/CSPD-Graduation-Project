package com.example.civilaffairs.ui.DepartmentServices.BirthServices.IssueBirth;

public class IssueClass {

    public String imageCardUrl;
    public String dateBirth;
    public String address;
    public String imageFamilyBookUrl ;
    public String pushKey;
    public String status;
    public String nationalId;


    public IssueClass() {
    }

    public void setImageCardUrl(String imageCardUrl) {
        this.imageCardUrl = imageCardUrl;
    }

    public void setDateBirth(String dateBirth) {
        this.dateBirth = dateBirth;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public void setImageFamilyBookUrl(String imageFamilyBookUrl) {
        this.imageFamilyBookUrl = imageFamilyBookUrl;
    }

    public void setPushKey(String pushKey) {
        this.pushKey = pushKey;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public void setNationalId(String nationalId) {
        this.nationalId = nationalId;
    }
}
